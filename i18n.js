  function getBrowserLanguage() {
    const raw =
      global.chrome?.i18n?.getUILanguage?.() ||
      global.navigator?.language ||
      DEFAULT_LANGUAGE;
    const normalized = String(raw).toLowerCase();
    const baseLanguage = normalized.split(/[-_]/)[0];
    return SUPPORTED_LANGUAGES.includes(baseLanguage) ? baseLanguage : DEFAULT_LANGUAGE;
  }

  function resolveLanguage(language) {
    if (SUPPORTED_LANGUAGES.includes(language)) return language;
    return getBrowserLanguage();
  }

